    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
       //Pega a linha selecionada
        int linhaSelecionada = jTable1.getSelectedRow();

        //Verifica se selecionou
        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(this,
                    "Por favor, selecione um curso na tabela para excluir.",
                    "Nenhum curso selecionado",
                    JOptionPane.WARNING_MESSAGE);
            return; 
        }

        //Pega o objeto Curso da lista
        Curso cursoSelecionado = this.listaDeCursos.get(linhaSelecionada);

        int confirmacao = JOptionPane.showConfirmDialog(
                this,
                "Tem certeza que deseja excluir o curso: \n" 
                    + cursoSelecionado.getNome() + "?",
                "Confirmação de Exclusão",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.WARNING_MESSAGE
        );

        //Se o usuário disse "Sim"
        if (confirmacao == JOptionPane.YES_OPTION) {
            
            CursoDAO cursoDAO = new CursoDAO();
            
            try {
                //TENTA remover pelo ID
                cursoDAO.excluir(cursoSelecionado);
                
                // Mostra sucesso E atualiza a tabela
                JOptionPane.showMessageDialog(this, 
                        "Curso removido com sucesso!", 
                        "Sucesso", 
                        JOptionPane.INFORMATION_MESSAGE);
                
                preencherTabela(null);

            } catch (Exception e) {
                // Se a LINHA 6 falhou:
                // Mostra o erro e NÃO FAZ NADA na tabela.
                JOptionPane.showMessageDialog(this,
                        "Erro ao excluir o curso:\n" + e.getMessage(),
                        "Erro no Banco de Dados",
                        JOptionPane.ERROR_MESSAGE);
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

