    /**
     * Busca os dados no DAO e preenche a JTable.
     */
    private void preencherTabela(String nomeBusca) {
        //Pega o "modelo" da sua tabela
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        
        //Limpa a tabela
        model.setRowCount(0); 

        //Buscar os dados no DAO
        CursoDAO cursoDAO = new CursoDAO();
        
        try { 
            //LÓGICA DA BUSCA:
            if (nomeBusca == null || nomeBusca.trim().isEmpty()) {
                // Se a busca for vazia, lista TODOS
                this.listaDeCursos = cursoDAO.listarTodos();
            } else {
                // Senão, lista os cursos FILTRADOS pelo nome
                this.listaDeCursos = cursoDAO.buscarPorNome(nomeBusca);
            }

            //Preenche a tabela com os dados da lista
            for (Curso curso : this.listaDeCursos) {
                model.addRow(new Object[]{
                    curso.getNome(),
                    curso.getCodigo()
                });
            }
            
        } catch (Exception e) { 
            //Tratamento de Exceção
            JOptionPane.showMessageDialog(this, 
                    "Erro ao carregar dados da tabela:\n" + e.getMessage(), 
                    "Erro", 
                    JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }

